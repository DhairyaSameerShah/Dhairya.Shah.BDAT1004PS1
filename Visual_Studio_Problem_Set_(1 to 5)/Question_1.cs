// Question 1 : What data type is each of the following?

using System;

namespace DP_ProblemSet_1
{
    class Question_1
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Indentify DataType \n");

            Console.WriteLine("The datatype of (5) is: Integer");
            Console.WriteLine("The datatype of (5.0) is: Float");
            Console.WriteLine("The datatype of (5 > 1) is: Boolean");
            Console.WriteLine("The datatype of ('5') is: String");
            Console.WriteLine("The datatype of (5 * 2) is: Integer");
            Console.WriteLine("The datatype of ('5' * 2) is: String");
            Console.WriteLine("The datatype of ('5' + '2') is: String");
            Console.WriteLine("The datatype of (5 / 2) is: Float");
            Console.WriteLine("The datatype of (5 % 2) is: Integer");
            Console.WriteLine("The datatype of {5, 2, 1} is: Set");
            Console.WriteLine("The datatype of (5 == 3) is: Boolean");
            Console.WriteLine("The datatype of (Pi [3.14]) is: Float");
        }
    }
}
